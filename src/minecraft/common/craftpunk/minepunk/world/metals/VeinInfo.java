package common.craftpunk.minepunk.world.metals;

public class VeinInfo
{
    public int veinCount;
    public int oreCount;
    public int minHeight;
    public int maxHeight;
    public int veinChance;
    public int veinDensity;
    
    public int getVeinCount()
    {
        return veinCount;
    }
    public int getOreCount()
    {
        return oreCount;
    }
    public int getMinHeight()
    {
        return minHeight;
    }
    public int getMaxHeight()
    {
        return maxHeight;
    }
    public int getVeinChance()
    {
        return veinChance;
    }
    public int getVeinDensity()
    {
        return veinDensity;
    }



}
